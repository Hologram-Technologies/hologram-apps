// Extract the IQ-quant codebook grids + helper tables from ggml-common.h and
// emit gguf-forge-iq-grids.mjs (base64-packed, deterministic). This is tooling,
// NOT runtime — run once after a llama.cpp bump:  node gen-iq-grids.mjs
//
// The C grids are uint64/uint32 arrays that the dequant code reinterprets as
// little-endian byte runs (`(const uint8_t*)(grid + idx)`). We flatten each to a
// byte array preserving that LE layout, so a grid lookup is just `base + j`.
//
// Authority: ggml/src/ggml-common.h (llama.cpp b7248 fork). Mechanical copy —
// no value is authored by hand.

import { readFileSync, writeFileSync } from "node:fs";

const HDR = process.argv[2] ||
  "C:/Users/pavel/Desktop/qvac-fabric-llm.cpp-master/qvac-fabric-llm.cpp-master/ggml/src/ggml-common.h";

const src = readFileSync(HDR, "utf8");

// Pull the value list out of a GGML_TABLE_BEGIN(<ctype>, <name>, <count>) ... GGML_TABLE_END().
function table(name) {
  const re = new RegExp(`GGML_TABLE_BEGIN\\(\\s*(\\w+)\\s*,\\s*${name}\\s*,[^)]*\\)([\\s\\S]*?)GGML_TABLE_END\\(\\)`);
  const m = src.match(re);
  if (!m) throw new Error("table not found: " + name);
  const ctype = m[1];
  const toks = m[2].replace(/\/\/[^\n]*/g, "").match(/-?0x[0-9a-fA-F]+|-?\d+/g) || [];
  return { ctype, toks };
}

// Flatten to LE bytes. width = bytes per element (8 for uint64, 4 for uint32, 1 for uint8/int8).
function bytesOf(name, width) {
  const { toks } = table(name);
  const out = new Uint8Array(toks.length * width);
  let o = 0;
  for (const t of toks) {
    let v = width >= 8 ? BigInt(t) : BigInt(parseInt(t, t.startsWith("-") || t.includes("x") ? undefined : 10));
    // signed decimals (kvalues_iq4nl) and any negative -> two's complement in `width` bytes
    if (v < 0n) v += 1n << BigInt(width * 8);
    for (let b = 0; b < width; b++) { out[o++] = Number(v & 0xffn); v >>= 8n; }
  }
  return out;
}

const grids = {
  iq2xxs_grid: bytesOf("iq2xxs_grid", 8),   // 256  u64
  iq2xs_grid:  bytesOf("iq2xs_grid", 8),    // 512  u64
  iq2s_grid:   bytesOf("iq2s_grid", 8),     // 1024 u64
  iq3xxs_grid: bytesOf("iq3xxs_grid", 4),   // 256  u32
  iq3s_grid:   bytesOf("iq3s_grid", 4),     // 512  u32
  iq1s_grid:   bytesOf("iq1s_grid", 8),     // 2048 u64 (read signed)
  ksigns_iq2xs: bytesOf("ksigns_iq2xs", 1), // 128  u8
  kmask_iq2xs:  bytesOf("kmask_iq2xs", 1),  // 8    u8
  kvalues_iq4nl: bytesOf("kvalues_iq4nl", 1), // 16 i8
};

// sanity: declared element counts
const want = { iq2xxs_grid: 256*8, iq2xs_grid: 512*8, iq2s_grid: 1024*8, iq3xxs_grid: 256*4,
  iq3s_grid: 512*4, iq1s_grid: 2048*8, ksigns_iq2xs: 128, kmask_iq2xs: 8, kvalues_iq4nl: 16 };
for (const [k, arr] of Object.entries(grids)) {
  if (arr.length !== want[k]) throw new Error(`${k}: got ${arr.length} bytes, want ${want[k]}`);
}

const b64 = (u8) => Buffer.from(u8).toString("base64");
let body = `// AUTO-GENERATED by gen-iq-grids.mjs from ggml-common.h — do not edit by hand.
// IQ-quant codebook grids + helper tables, flattened to little-endian byte runs.
// A grid entry \`idx\` of stride S occupies bytes [idx*S .. idx*S+S); read uint8
// for iq2*/iq3* grids, int8 (\`(b<<24)>>24\`) for iq1s_grid / kvalues_iq4nl.
const D = (s) => Uint8Array.from(atob(s), (c) => c.charCodeAt(0));
`;
for (const [k, arr] of Object.entries(grids)) {
  body += `export const ${k} = /* ${arr.length}B */ D("${b64(arr)}");\n`;
}
const outPath = new URL("./gguf-forge-iq-grids.mjs", import.meta.url);
writeFileSync(outPath, body);
console.log("wrote gguf-forge-iq-grids.mjs:", Object.fromEntries(Object.entries(grids).map(([k, v]) => [k, v.length])));
